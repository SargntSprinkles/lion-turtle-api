CREATE TABLE characters (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    deleted_at TIMESTAMP,
    user_google_id VARCHAR(50),
    character_name VARCHAR(50),
    playbook VARCHAR(50),
    training_water BOOLEAN,
    training_earth BOOLEAN,
    training_fire BOOLEAN,
    training_air BOOLEAN,
    training_weapons BOOLEAN,
    training_tech BOOLEAN,
    fighting_style TEXT,
    background_military BOOLEAN,
	background_monastic BOOLEAN,
	background_outlaw BOOLEAN,
	background_privileged BOOLEAN,
	background_urban BOOLEAN,
	background_wilderness BOOLEAN,
	hometown VARCHAR(50)
);